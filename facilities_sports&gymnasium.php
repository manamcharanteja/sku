   <h1 align="center" class="container p-4"><font color="green">Sports - Gymnasium</font></h1>

   <p class="container" style="font-size: 15px;text-align: justify;">The University College provides facilities for a wide range of sports and games. It has a modern gymnasium which is equipped with the latest gymnastic and fitness gadgets like the multigym. The Gym provides facilities for indoor games like shuttle badminton, table-tennis, chess, carroms, besides Weightlifting and malkhamb. The renovated University stadium provides facilities for cricket, foot ball, and athletics. There are also courts for ball badminton, basket ball, volley ball, tennis, etc.
    The University hosts All India and South zone Inter-University games competitions regularly. Intramurals are conducted annually. In addition, intercollegiate sports and games are conducted quite frequently in the campus. Coaching camps in different disciplines are held periodically in an atmosphere congenial to the development of skills in different sports and games.</p>

