<p style="text-align:justify";> The Department of Botany has Internationally recognized Herbarium with an acronym ‘SKU’ by Index Herbariorum, a global directory of herbaria of world. It houses about 33,000 specimens collected during 1983-2015 from different parts of Southern Peninsular India and Andaman Islands. Most of the collections are made through research projects sponsored by national funding agencies like UGC, DST, CSIR and DBT. </p>          
